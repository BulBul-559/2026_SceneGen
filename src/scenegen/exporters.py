def sanitize_path_segment(value: str) -> str:
    safe = re.sub(r"[^A-Za-z0-9._-]+", "_", value).strip("._")
    return safe or "item"


def sanitize_xml_id(value: str) -> str:
    safe = re.sub(r"[^A-Za-z0-9_.-]+", "_", value).strip("._-")
    if not safe:
        return "shape"
    if not re.match(r"[A-Za-z_]", safe):
        safe = f"shape_{safe}"
    return safe


def format_xml_float(value: float) -> str:
    if abs(value) < 1e-10:
        value = 0.0
    return f"{value:.9g}"


def relative_posix(path: Path, root: Path) -> str:
    return path.relative_to(root).as_posix()


def obj_vertex_lines(vertices: Iterable[Sequence[float]]) -> list[str]:
    return [f"v {x:.6f} {y:.6f} {z:.6f}\n" for x, y, z in vertices]


def obj_face_lines(faces: list[list[int]], vertex_offset: int = 0) -> list[str]:
    return ["f " + " ".join(str(vertex_offset + index + 1) for index in face) + "\n" for face in faces]


def triangulated_face_indices(faces: list[list[int]], vertex_offset: int = 0) -> list[tuple[int, int, int]]:
    triangles: list[tuple[int, int, int]] = []
    for face in faces:
        if len(face) < 3:
            continue
        first = int(face[0]) + vertex_offset
        for index in range(1, len(face) - 1):
            triangles.append((first, int(face[index]) + vertex_offset, int(face[index + 1]) + vertex_offset))
    return triangles


def write_clean_obj_subset(
    path: Path,
    object_name: str,
    vertices: list[tuple[float, float, float]],
    faces: list[list[int]],
) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    index_map: dict[int, int] = {}
    subset_vertices: list[tuple[float, float, float]] = []
    for face in faces:
        for index in face:
            if index < 0 or index >= len(vertices):
                raise ValueError(f"OBJ face index out of range while writing {path}: {index}")
            if index not in index_map:
                index_map[index] = len(subset_vertices) + 1
                subset_vertices.append(vertices[index])

    with path.open("w", encoding="utf-8", newline="\n") as handle:
        handle.writelines(
            [
                "# Generated by SceneGen for Sionna RT\n",
                f"o {sanitize_xml_id(object_name)}\n",
                *obj_vertex_lines(subset_vertices),
                *("f " + " ".join(str(index_map[index]) for index in face) + "\n" for face in faces),
            ]
        )


def write_clean_obj_full(
    path: Path,
    object_name: str,
    vertices: list[tuple[float, float, float]],
    faces: list[list[int]],
) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8", newline="\n") as handle:
        handle.write("# Generated by SceneGen for Sionna RT\n")
        handle.write(f"o {sanitize_xml_id(object_name)}\n")
        handle.writelines(obj_vertex_lines(vertices))
        handle.writelines(obj_face_lines(faces))


def write_clean_obj_full_from_source(path: Path, object_name: str, source_obj: Path) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with source_obj.open("r", encoding="utf-8", errors="ignore") as source, path.open(
        "w",
        encoding="utf-8",
        newline="\n",
    ) as handle:
        handle.write("# Generated by SceneGen for Sionna RT\n")
        handle.write(f"o {sanitize_xml_id(object_name)}\n")
        for line in source:
            if line.startswith(("v ", "vt ", "vn ", "f ")):
                handle.write(line if line.endswith("\n") else f"{line}\n")


def write_clean_obj_mesh(path: Path, object_name: str, mesh: ObjMesh) -> None:
    write_clean_obj_subset(path, object_name, mesh.vertices, mesh.faces)


def transform_matrix_for_placement(placed: PlacedAsset) -> tuple[float, ...]:
    if placed.transform_matrix_4x4_row_major is not None:
        return placed.transform_matrix_4x4_row_major

    cos_yaw = math.cos(placed.yaw)
    sin_yaw = math.sin(placed.yaw)
    return (
        cos_yaw,
        -sin_yaw,
        0.0,
        placed.x,
        sin_yaw,
        cos_yaw,
        0.0,
        placed.y,
        0.0,
        0.0,
        1.0,
        placed.z,
        0.0,
        0.0,
        0.0,
        1.0,
    )


def transform_vertices_array(mesh: ObjMesh, placed: PlacedAsset) -> np.ndarray:
    vertices = np.asarray(mesh.vertices, dtype=np.float64)
    if vertices.size == 0:
        return vertices.reshape((0, 3))
    if placed.transform_matrix_4x4_row_major is not None:
        matrix = np.asarray(placed.transform_matrix_4x4_row_major, dtype=np.float64).reshape((4, 4))
        return vertices @ matrix[:3, :3].T + matrix[:3, 3]

    cos_yaw = math.cos(placed.yaw)
    sin_yaw = math.sin(placed.yaw)
    transformed = np.empty_like(vertices)
    transformed[:, 0] = placed.x + cos_yaw * vertices[:, 0] - sin_yaw * vertices[:, 1]
    transformed[:, 1] = placed.y + sin_yaw * vertices[:, 0] + cos_yaw * vertices[:, 1]
    transformed[:, 2] = placed.z + vertices[:, 2]
    return transformed


def export_sionna_asset_parts(
    scene_dir: Path,
    assets_dir: Path,
    asset: Asset,
    cache: dict[Path, list[SionnaAssetPart]],
) -> list[SionnaAssetPart]:
    cached = cache.get(asset.obj_file)
    if cached is not None:
        return cached

    material_mesh = load_obj_material_mesh(asset.obj_file)
    faces_by_sionna: dict[str, list[list[int]]] = {}
    total_face_count = 0
    for source_material, faces in material_mesh.faces_by_material.items():
        total_face_count += len(faces)
        material_name = resolve_face_sionna_material(asset, source_material)
        faces_by_sionna.setdefault(material_name, []).extend(faces)

    asset_dir_name = sanitize_path_segment(asset.export_name)
    asset_dir = assets_dir / asset_dir_name
    asset_stem = sanitize_path_segment(asset.export_name)
    parts: list[SionnaAssetPart] = []
    for material_name in sorted(faces_by_sionna):
        faces = faces_by_sionna[material_name]
        material_suffix = sanitize_path_segment(sionna_material_itu_type(material_name))
        path = asset_dir / f"{asset_stem}__{material_suffix}.obj"
        if len(faces_by_sionna) == 1 and len(faces) == total_face_count:
            write_clean_obj_full_from_source(path, f"{asset.export_name}_{material_suffix}", asset.obj_file)
        else:
            write_clean_obj_subset(
                path,
                f"{asset.export_name}_{material_suffix}",
                material_mesh.vertices,
                faces,
            )
        parts.append(
            SionnaAssetPart(
                filename=relative_posix(path, scene_dir),
                material_name=material_name,
                face_count=len(faces),
            )
        )

    cache[asset.obj_file] = parts
    return parts


def export_front3d_base_parts(
    scene_dir: Path,
    assets_dir: Path,
    base_scene: Front3DBaseScene,
) -> list[SionnaAssetPart]:
    material_mesh = load_obj_material_mesh(base_scene.scene_obj)
    material_lookup: dict[str, str] = {}
    for source, material_name in base_scene.source_to_sionna_material.items():
        material_lookup[source] = material_name
        material_lookup[sanitize_path_segment(source)] = material_name

    base_dir = assets_dir / "empty_scene"
    offset_x, offset_y, offset_z = base_scene.world_offset
    vertices = [(x + offset_x, y + offset_y, z + offset_z) for x, y, z in material_mesh.vertices]
    parts: list[SionnaAssetPart] = []
    for source_material, faces in sorted(material_mesh.faces_by_material.items(), key=lambda item: str(item[0])):
        material_name = material_lookup.get(source_material or "", SIONNA_BASE_SCENE_MATERIAL)
        material_suffix = sanitize_path_segment(sionna_material_itu_type(material_name))
        source_suffix = sanitize_path_segment(source_material or "default")
        path = base_dir / f"front3d_architecture__{source_suffix}__{material_suffix}.obj"
        write_clean_obj_subset(
            path,
            f"front3d_architecture_{source_suffix}_{material_suffix}",
            vertices,
            faces,
        )
        parts.append(
            SionnaAssetPart(
                filename=relative_posix(path, scene_dir),
                material_name=material_name,
                face_count=len(faces),
            )
        )
    return parts


def build_sionna_scene_xml(shapes: list[SionnaXmlShape]) -> str:
    scene = ET.Element("scene", version="3.0.0")
    for material_name in sorted({shape.material_name for shape in shapes}):
        bsdf = ET.SubElement(scene, "bsdf", type="itu-radio-material", id=material_name)
        ET.SubElement(bsdf, "string", name="type", value=sionna_material_itu_type(material_name))

    for shape_info in shapes:
        shape = ET.SubElement(scene, "shape", type="obj", id=sanitize_xml_id(shape_info.shape_id))
        ET.SubElement(shape, "string", name="filename", value=shape_info.filename)
        if shape_info.transform is not None:
            transform = ET.SubElement(shape, "transform", name="to_world")
            ET.SubElement(
                transform,
                "matrix",
                value=" ".join(format_xml_float(value) for value in shape_info.transform),
            )
        ET.SubElement(shape, "ref", id=shape_info.material_name)

    rough = ET.tostring(scene, encoding="utf-8")
    return minidom.parseString(rough).toprettyxml(indent="  ", encoding="utf-8").decode("utf-8")


def write_sionna_scene_assets(
    output_dir: Path,
    base_mesh: ObjMesh,
    placements: list[PlacedAsset],
) -> dict[str, object]:
    assets_dir = output_dir / "assets"
    empty_scene_obj = assets_dir / "empty_scene" / "scene.obj"
    write_clean_obj_mesh(empty_scene_obj, "empty_scene", base_mesh)

    shapes = [
        SionnaXmlShape(
            shape_id="empty_scene",
            filename=relative_posix(empty_scene_obj, output_dir),
            material_name=SIONNA_BASE_SCENE_MATERIAL,
        )
    ]
    asset_cache: dict[Path, list[SionnaAssetPart]] = {}
    placement_asset_parts: list[dict[str, object]] = []
    for placed in placements:
        parts = export_sionna_asset_parts(output_dir, assets_dir, placed.asset, asset_cache)
        transform = transform_matrix_for_placement(placed)
        placement_parts: list[dict[str, object]] = []
        for part in parts:
            material_suffix = sanitize_path_segment(sionna_material_itu_type(part.material_name))
            shape_id = f"{placed.instance_name}_{material_suffix}"
            shapes.append(
                SionnaXmlShape(
                    shape_id=shape_id,
                    filename=part.filename,
                    material_name=part.material_name,
                    transform=transform,
                )
            )
            placement_parts.append(
                {
                    "shape_id": sanitize_xml_id(shape_id),
                    "asset_obj": part.filename,
                    "sionna_material_name": part.material_name,
                    "itu_type": sionna_material_itu_type(part.material_name),
                    "face_count": part.face_count,
                }
            )
        placement_asset_parts.append(
            {
                "instance_name": placed.instance_name,
                "asset_name": placed.asset.name,
                "parts": placement_parts,
            }
        )

    (output_dir / "scene.xml").write_text(build_sionna_scene_xml(shapes), encoding="utf-8")
    return {
        "assets_dir": portable_path(assets_dir, output_dir),
        "empty_scene_obj": relative_posix(empty_scene_obj, output_dir),
        "scene_xml": portable_path(output_dir / "scene.xml", output_dir),
        "shape_count": len(shapes),
        "asset_file_count": sum(len(parts) for parts in asset_cache.values()) + 1,
        "materials": sorted({shape.material_name for shape in shapes}),
        "placements": placement_asset_parts,
    }


