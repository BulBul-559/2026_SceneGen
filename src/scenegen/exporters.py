def write_front3d_scene_files(
    output_dir: Path,
    base_scene: Front3DBaseScene,
    placements: list[PlacedAsset],
    skipped_objects: list[dict[str, object]],
    scene_index: int,
    seed: int,
    rng: object,
    collect_floorplan_mesh_arrays: bool = True,
    mode: str = "front3d",
    write_scene_obj: bool = True,
    write_sionna_assets: bool = True,
) -> tuple[dict[str, object], SceneMeshArrays | None]:
    timings_s: dict[str, float] = {}
    output_dir.mkdir(parents=True, exist_ok=True)
    started = time.perf_counter()
    if write_scene_obj:
        floorplan_mesh_arrays = write_front3d_obj(
            output_dir / "scene.obj",
            base_scene,
            placements,
            collect_mesh_arrays=collect_floorplan_mesh_arrays,
            mode=mode,
        )
    elif collect_floorplan_mesh_arrays:
        floorplan_mesh_arrays = collect_front3d_scene_mesh_arrays(base_scene, placements)
    else:
        floorplan_mesh_arrays = None
    timings_s["write_scene_obj"] = round(time.perf_counter() - started, 6)
    started = time.perf_counter()
    if write_sionna_assets:
        sionna_assets = write_front3d_sionna_scene_assets(output_dir, base_scene, placements)
    else:
        sionna_assets = {
            "shape_count": 0,
            "materials": [],
            "asset_parts": [],
            "skipped": True,
            "reason": "output.write_sionna_assets=false",
        }
    timings_s["write_sionna_assets"] = round(time.perf_counter() - started, 6)
    started = time.perf_counter()
    placement_payload = {
        "scene_index": scene_index,
        "seed": seed,
        "mode": mode,
        "front3d_scene_id": base_scene.scene_id,
        "source_scene": portable_path(base_scene.source_scene_json, output_dir),
        "architecture": base_scene_summary(base_scene, output_dir),
        "skipped_objects": skipped_objects,
        "skipped_object_count": len(skipped_objects),
        "sionna_assets": sionna_assets,
        "placement_count": len(placements),
        "placements": [placement_to_json(placement, output_dir) for placement in placements],
    }
    (output_dir / "placements.json").write_text(json.dumps(placement_payload, ensure_ascii=False, indent=2), encoding="utf-8")
    timings_s["write_placements_json"] = round(time.perf_counter() - started, 6)
    record = scene_record(output_dir, scene_index, seed, sionna_assets, placements)
    record["front3d_scene_id"] = base_scene.scene_id
    if mode != "front3d":
        record["mode"] = mode
    record["source_scene"] = portable_path(base_scene.source_scene_json, output_dir.parent)
    record["skipped_object_count"] = len(skipped_objects)
    record["skipped_objects"] = skipped_objects
    if not write_scene_obj:
        record["scene_obj"] = None
    if not write_sionna_assets:
        record["scene_xml"] = None
        record["assets_dir"] = None
        record["sionna_assets_skipped"] = True
    record["build_timings_s"] = timings_s
    return record, floorplan_mesh_arrays


def scene_record(
    output_dir: Path,
    scene_index: int,
    seed: int,
    sionna_assets: dict[str, object],
    placements: list[PlacedAsset],
) -> dict[str, object]:
    run_dir = output_dir.parent
    return {
        "scene_index": scene_index,
        "seed": seed,
        "scene_dir": portable_path(output_dir, run_dir),
        "scene_obj": portable_path(output_dir / "scene.obj", run_dir),
        "scene_xml": portable_path(output_dir / "scene.xml", run_dir),
        "assets_dir": portable_path(output_dir / "assets", run_dir),
        "sionna_shape_count": sionna_assets["shape_count"],
        "sionna_materials": sionna_assets["materials"],
        "placement_count": len(placements),
    }


def base_scene_summary(base_scene: BistroBaseScene | Front3DBaseScene, path_root: Path | None = None) -> dict[str, object]:
    if isinstance(base_scene, Front3DBaseScene):
        root = path_root or base_scene.scene_obj.parent
        return {
            "scene_id": base_scene.scene_id,
            "scene_obj": portable_path(base_scene.scene_obj, root),
            "source_scene_json": portable_path(base_scene.source_scene_json, root),
            "metadata_json": portable_path(base_scene.metadata_json, root),
            "bbox_min": [round(value, 6) for value in base_scene.bbox_min],
            "bbox_max": [round(value, 6) for value in base_scene.bbox_max],
            "source_bbox_min": [round(value, 6) for value in base_scene.source_bbox_min],
            "source_bbox_max": [round(value, 6) for value in base_scene.source_bbox_max],
            "world_offset": [round(value, 6) for value in base_scene.world_offset],
            "materials": list(base_scene.sionna_material_names),
        }

    root = path_root or base_scene.base_dir
    return {
        "base_dir": portable_path(base_scene.base_dir, root),
        "scene_obj": portable_path(base_scene.scene_obj, root),
        "bbox_min": [round(value, 6) for value in base_scene.bbox_min],
        "bbox_max": [round(value, 6) for value in base_scene.bbox_max],
        "floor_z": round(base_scene.floor_z, 6),
        "floor_triangle_count": len(base_scene.floor_triangles),
        "floor_area": round(sum(tri.area for tri in base_scene.floor_triangles), 6),
        "support_surface_count": len(base_scene.support_surfaces),
        "static_obstacle_count": len(base_scene.static_obstacles),
    }


def make_timestamp() -> str:
    return dt.datetime.now().strftime("%Y%m%d%H%M%S")


def height_token(value: float) -> str:
    return f"{value:g}".replace("-", "m").replace(".", "p")


def collect_scene_objs(run_dir: Path, scene_records: list[dict[str, object]], scene_prefix: str) -> dict[str, object]:
    summary_dir = run_dir / "summary" / "obj"
    summary_dir.mkdir(parents=True, exist_ok=True)
    copied: list[dict[str, str]] = []
    for record in scene_records:
        scene_index = int(record["scene_index"])
        scene_obj = record.get("scene_obj")
        if not isinstance(scene_obj, str) or not scene_obj:
            continue
        source = Path(scene_obj)
        if not source.is_absolute():
            source = run_dir / source
        if not source.is_file():
            continue
        destination = summary_dir / f"{scene_prefix}_{scene_index:04d}.obj"
        shutil.copy2(source, destination)
        copied.append(
            {
                "source": portable_path(source, run_dir),
                "destination": portable_path(destination, run_dir),
            }
        )

    copy_manifest = {
        "summary_dir": portable_path(summary_dir, run_dir),
        "count": len(copied),
        "objects": copied,
    }
    (summary_dir / "copy_manifest.json").write_text(
        json.dumps(copy_manifest, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    return copy_manifest


def collect_raw_floorplans(run_dir: Path, scene_records: list[dict[str, object]], scene_prefix: str) -> dict[str, object]:
    summary_dir = run_dir / "summary" / "floorplan"
    summary_dir.mkdir(parents=True, exist_ok=True)
    copied: list[dict[str, str]] = []

    for record in scene_records:
        scene_index = int(record["scene_index"])
        floorplan = record.get("floorplan")
        if not isinstance(floorplan, dict) or not floorplan.get("ok", False):
            continue
        geometry = floorplan.get("geometry")
        if not isinstance(geometry, dict):
            continue
        raw_path = geometry.get("raw")
        if not isinstance(raw_path, str) or not raw_path:
            continue

        scene_dir = Path(str(record["scene_dir"]))
        if not scene_dir.is_absolute():
            scene_dir = run_dir / scene_dir
        source = Path(raw_path)
        if not source.is_absolute():
            source = scene_dir / source
        if not source.is_file():
            continue

        destination = summary_dir / f"{scene_prefix}_{scene_index:04d}_{source.name}"
        shutil.copy2(source, destination)
        copied.append(
            {
                "scene_index": str(scene_index),
                "source": portable_path(source, run_dir),
                "destination": portable_path(destination, run_dir),
            }
        )

    copy_manifest = {
        "summary_dir": portable_path(summary_dir, run_dir),
        "count": len(copied),
        "images": copied,
    }
    (summary_dir / "copy_manifest.json").write_text(
        json.dumps(copy_manifest, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    return copy_manifest


def collect_label_floorplans(run_dir: Path, scene_records: list[dict[str, object]], scene_prefix: str) -> dict[str, object]:
    summary_dir = run_dir / "summary" / "label_floorplan"
    summary_dir.mkdir(parents=True, exist_ok=True)
    copied: list[dict[str, str]] = []
    groups: dict[str, dict[str, object]] = {}

    for record in scene_records:
        scene_index = int(record["scene_index"])
        label = record.get("label")
        if not isinstance(label, dict) or not label.get("ok", False):
            continue
        variants = label.get("variants")
        if not isinstance(variants, list):
            continue
        for variant in variants:
            if not isinstance(variant, dict):
                continue
            overlay = variant.get("overlay")
            if not isinstance(overlay, dict):
                continue
            overlay_path = overlay.get("image")
            if not isinstance(overlay_path, str) or not overlay_path:
                continue
            grid_resolution = variant.get("grid_resolution_m")
            if not isinstance(grid_resolution, int | float):
                continue
            source = Path(overlay_path)
            if not source.is_absolute():
                source = run_dir / source
            if not source.is_file():
                continue

            grid_name = height_token(float(grid_resolution))
            grid_dir = summary_dir / grid_name
            grid_dir.mkdir(parents=True, exist_ok=True)
            variant_name = str(variant.get("name") or source.stem)
            destination = grid_dir / f"{scene_prefix}_{scene_index:04d}_{variant_name}.png"
            shutil.copy2(source, destination)
            item = {
                "scene_index": str(scene_index),
                "variant": variant_name,
                "grid_resolution_m": f"{float(grid_resolution):g}",
                "source": portable_path(source, run_dir),
                "destination": portable_path(destination, run_dir),
            }
            copied.append(item)
            group = groups.setdefault(
                grid_name,
                {
                    "grid_resolution_m": float(grid_resolution),
                    "directory": portable_path(grid_dir, run_dir),
                    "count": 0,
                    "images": [],
                },
            )
            group["count"] = int(group["count"]) + 1
            group_images = group["images"]
            if isinstance(group_images, list):
                group_images.append(item)

    copy_manifest = {
        "summary_dir": portable_path(summary_dir, run_dir),
        "count": len(copied),
        "groups": groups,
        "images": copied,
    }
    (summary_dir / "copy_manifest.json").write_text(
        json.dumps(copy_manifest, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )
    return copy_manifest


def _scene_record_dir(run_dir: Path, record: dict[str, object], scene_prefix: str) -> Path:
    scene_dir_value = record.get("scene_dir")
    if isinstance(scene_dir_value, str) and scene_dir_value:
        scene_dir = Path(scene_dir_value)
        return scene_dir if scene_dir.is_absolute() else run_dir / scene_dir
    return run_dir / f"{scene_prefix}_{int(record.get('scene_index', 0)):04d}"


def _resolve_visual_path(run_dir: Path, scene_dir: Path, value: object) -> str | None:
    if not isinstance(value, str) or not value:
        return None
    candidate = Path(value)
    if candidate.is_absolute():
        source = candidate
    elif (run_dir / candidate).is_file():
        source = run_dir / candidate
    else:
        source = scene_dir / candidate
    if not source.is_file():
        return None
    return portable_path(source, run_dir)


def _html_attr(value: object) -> str:
    return html.escape(str(value), quote=True)


def _html_text(value: object) -> str:
    return html.escape(str(value), quote=False)


def _html_href(path: str) -> str:
    return quote(path, safe="/._-")


def _visual_metric_items(record: dict[str, object]) -> list[tuple[str, object]]:
    metrics: list[tuple[str, object]] = []
    metrics.append(("placements", record.get("placement_count", len(record.get("placements", [])) if isinstance(record.get("placements"), list) else "")))
    quality = record.get("quality")
    if isinstance(quality, dict):
        metrics.append(("quality errors", quality.get("error_count", "")))
    label = record.get("label")
    if isinstance(label, dict):
        metrics.append(("labels", f"BS {label.get('bs_count', 0)} / UE {label.get('ue_count', 0)}"))
    procedural = record.get("procedural")
    if isinstance(procedural, dict):
        if procedural.get("layout"):
            metrics.append(("layout", procedural.get("layout", "")))
        rooms = procedural.get("rooms")
        if isinstance(rooms, list):
            metrics.append(("rooms", len(rooms)))
    return [(key, value) for key, value in metrics if value not in (None, "")]


def write_visual_index(run_dir: Path, scene_records: list[dict[str, object]], scene_prefix: str) -> str:
    """Write a lightweight HTML gallery for generated visual QA artifacts."""

    cards: list[str] = []
    for record in sorted(scene_records, key=lambda item: int(item.get("scene_index", 0))):
        scene_index = int(record.get("scene_index", 0))
        scene_key = str(record.get("scene_key") or record.get("scene_dir") or f"{scene_prefix}_{scene_index:04d}")
        scene_dir = _scene_record_dir(run_dir, record, scene_prefix)
        floorplan = record.get("floorplan")
        images: list[tuple[str, str]] = []
        if isinstance(floorplan, dict):
            geometry = floorplan.get("geometry")
            if isinstance(geometry, dict):
                raw_path = _resolve_visual_path(run_dir, scene_dir, geometry.get("raw"))
                if raw_path:
                    images.append(("floorplan", raw_path))
            class_mask = floorplan.get("class_mask")
            if isinstance(class_mask, dict):
                class_preview = _resolve_visual_path(run_dir, scene_dir, class_mask.get("preview"))
                if class_preview:
                    images.append(("class mask", class_preview))
            label_overlays = floorplan.get("label_overlays")
            if isinstance(label_overlays, list):
                for overlay in label_overlays:
                    if not isinstance(overlay, dict):
                        continue
                    overlay_path = _resolve_visual_path(run_dir, scene_dir, overlay.get("image"))
                    if overlay_path:
                        images.append((str(overlay.get("name") or "label overlay"), overlay_path))
        if not images:
            label = record.get("label")
            if isinstance(label, dict):
                variants = label.get("variants")
                if isinstance(variants, list):
                    for variant in variants:
                        if not isinstance(variant, dict):
                            continue
                        overlay = variant.get("overlay")
                        if not isinstance(overlay, dict):
                            continue
